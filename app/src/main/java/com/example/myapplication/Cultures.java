package com.example.myapplication;

/**
 * Represents a culture with its image.
 */
public class Cultures {

    /**
     * The resource ID of the culture image.
     */
    public int m_CultureImage;

    /**
     * Constructs a new Cultures object with the given culture image.
     *
     * @param cultureImage The resource ID of the culture image.
     */
    public Cultures(int cultureImage) {
        this.m_CultureImage = cultureImage;
    }
}
